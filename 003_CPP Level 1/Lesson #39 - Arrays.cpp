#include <iostream>
#include <string>

using namespace std;

void ReadGrades(float Grades[3])
{
	cout << " Please enter Grade 1 : "; cin >> Grades[0];
	cout << " Please enter Grade 2 : "; cin >> Grades[1];
	cout << " Please enter Grade 3 : "; cin >> Grades[2];

}

float CalculeTheGrade(float Grades[3])
{
	return (Grades[0] + Grades[1] + Grades[2]) / 3;
}

void PrintResulte(float Result)
{
	cout << "**********************************\n";
	cout << " The average of grades is : " << Result << endl;
}

int main()
{
	float Grades[3];

	ReadGrades(Grades);
	PrintResulte(CalculeTheGrade(Grades));



	return 0;
}
