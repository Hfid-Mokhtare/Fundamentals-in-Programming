#include <iostream>
using namespace std;


int main()
{
	int A = 10, B = 20;
	
	A += B;//A = A + B
	cout << " A = " << A << endl;

	A -= B;//A = A - B
	cout << " A = " << A << endl;

	A *= B;//A = A * B
	cout << " A = " << A << endl;

	A /= B;//A = A / B
	cout << " A = " << A << endl;

	A %= B;//A = A % B
	cout << " A = " << A << endl;


	return 0;
}

//resulte :

// A = 30
// A = 10
// A = 200
// A = 10
