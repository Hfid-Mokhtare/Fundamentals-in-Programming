#include <iostream>
using namespace std;


int main()
{
	short num1, num2;

	cout << "enter the first number : "; cin >> num1;
	cout << "enter the second number : "; cin >> num2;

	cout << num1 << " = " << num2 << " is " <<(num1==num2)<<endl;
	cout << num1 << " != " << num2 << " is " << (num1 != num2) << endl;
	cout << num1 << " > " << num2 << " is " << (num1 > num2) << endl;
	cout << num1 << " < " << num2 << " is " << (num1 < num2) << endl;
	cout << num1 << " >= " << num2 << " is " << (num1 >= num2) << endl;
	cout << num1 << " <= " << num2 << " is " << (num1 <= num2) << endl;



	return 0;
}
//Relational Opetatores :
// ==
// !=
// >
// <
