#include <iostream>
#include <string>
#include <cstdlib>

using namespace std;

int PrintRandomNumber(int From,int To)
{
	int randNum = rand() % (To - From + 1) + From;
	return randNum;
}

int main()
{
	srand((unsigned)time(NULL));

	
	cout << PrintRandomNumber(1, 10) << endl;
	cout << PrintRandomNumber(1, 10) << endl;
	cout << PrintRandomNumber(1, 10) << endl;

	return 0;
}
